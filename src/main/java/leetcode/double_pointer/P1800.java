public class P1800 {
    public int maxAscendingSum(int[] nums) {
        int max = 0, len = nums.length;
        if (1 == len){
            return nums[0];
        }
        int i = 0, j = 1;
        while (j < len){
            while (j < len && nums[j-1] < nums[j] ){
                j++;
            }
            int tmp = 0;
            while (i < j){
                tmp += nums[i];
                i++;
            }
            if (tmp > max){
                max = tmp;
            }
            j++;
        }

        return max;
    }

    public static void main(String[] args) {
        P1800 sou = new P1800();
        int[] nums = {3,6,10,1,8,9,9,8,9};
        int max = sou.maxAscendingSum(nums);
        System.out.println(max);


    }
}
