    public int numRookCaptures(char[][] board) {
        int rows = board.length, cols = board[0].length;
        int count = 0;
        for (int r = 0; r < rows; r++){
            for (int c = 0; c < cols; c++){

                if ('R' != board[r][c]){
                    continue;
                }
                //å‘å³
                for (int i = c + 1; i < rows; i++){
//                    System.out.println(board[r][i]);
                    if (board[r][i] == '.'){
                        continue;
                    }
                    if (board[r][i] == 'p'){
                        count++;
                    }
                    break;
                }
                //å‘å·¦
                for (int i = c - 1; i >= 0; i--){
                    if (board[r][i] == '.'){
                        continue;
                    }
                    if (board[r][i] == 'p'){
                        count++;
                    }
                    break;
                }

                //å‘ä¸‹
                for (int i = r + 1; i < rows; i++){
                    if (board[i][c] == '.'){
                        continue;
                    }
                    if (board[i][c] == 'p'){
                        count++;
                    }
                    break;
                }
                //å‘ä¸Š
                for (int i = r - 1; i >= 0; i--){
                    if (board[i][c] == '.'){
                        continue;
                    }
                    if (board[i][c] == 'p'){
                        count++;
                    }
                    break;
                }

            }
        }

        return count;
    }

    public int numRookCaptures1(char[][] board) {
        int cnt = 0, st = 0, ed = 0;
        int[] dx = {0, 1, 0, -1};
        int[] dy = {1, 0, -1, 0};
        for (int i = 0; i < 8; i++){
            for (int j = 0; j < 8; j++){
                if (board[i][j] == 'R'){
                    st = i;
                    ed = j;
                    break;
                }
            }
        }
        for (int i = 0; i < 4; i++){
            for (int step = 0; ; step++){
                int tx = st + step * dx[i];
                int ty = ed + step * dy[i];
                if (tx < 0 || tx >= 8 || ty < 0 || ty >= 8 || board[tx][ty] == 'B'){
                    break;
                }
                if (board[tx][ty] == 'p'){
                    cnt++;
                    break;
                }
            }
        }

        return cnt;
    }
